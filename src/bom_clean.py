# BOM → строка для станка / экспорт колонок (в разработке).
#
# Примеры целевого формата part (без пробелов/запятых, латиница+цифры+_%):
#   240R+1\\16W+±1%+0402  →  0402_240_1%
#   22UF+25V+±20%(M)+0805+X5R  →  0805_22uF_25V_X5R_20%
#   voltage-regulator diode+SMA+SMA4734A  →  SMA4734A
#
# Целевая таблица: part | job (smt/tht/assembly) | pics_per_unit | ref | inventory (+0005856)

CLEAN_PLACEHOLDER_MESSAGE = """Clean — пока заглушка.

Дальнейший алгоритм (черновик):
• Разбор поля comment/value по «+», нормализация слэшей и ±.
• Выделить корпус (0402/0805/SMA…) → префикс part для станка.
• Толерансы: ±1% → 1%; ±20%(M) → 20%.
• Инвентарный номер вида +0005856 → отдельная колонка, из part убрать.
• job: из слоя BOM / префикса designator / отдельной колонки → smt | tht | assembly.
• pics per unit: из количества BOM или 1 по умолчанию.
• ref: designator / Plug-in position без пробелов.
• Удаление CJK, запятых, пробелов; только допустимые символы для станка.

Для диодов/регуляторов: последний токен с номером типа SMA4734A как краткое имя."""


def get_clean_placeholder_message() -> str:
    return CLEAN_PLACEHOLDER_MESSAGE
